#include <stdio.h>

//-------------------------------------------------------------

void PrintMassive (int data[][4]);

//-------------------------------------------------------------

int main ()
    {
    int data[5][4] = {{10, 11, 12, 13}, 
                      {20, 21, 22, 23}, 
                      {30, 31, 32, 33}, 
                      {40, 41, 42, 43}, 
                      {50, 51, 52, 53}};

    PrintMassive (data);
    }

//-------------------------------------------------------------

void PrintMassive (int data[][4])
    {
    for (int y = 0; y < 5; y++)
        {
        for (int x = 0; x < 4; x++)
            {
            printf ("data[%d][%d] = %d \n", y, x, data[y][x]);
            }
        }
    }